# Задание 8
# Нарисовать в консоли ёлку спросив у пользователя количество рядов.
# Пример результата:
# Сколько рядов у ёлки? 5
#    *
#   ***
#  *****
# *******
#*********

print('Задание 8')

while True:
    try:
        n = int(input('Сколько рядов у ёлки? '))
        if n < 0:
            raise ValueError
        break
    except ValueError:
        print('Введены некорректные данные. Попробуйте еще раз.')

for i in range(n):
    print(' ' * (n - 1 - i) + '*' * (2 * i + 1))

