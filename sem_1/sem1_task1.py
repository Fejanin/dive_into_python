# Задание 5
# Посчитайте сумму чётных элементов от 1 до n исключая кратные e.
# Используйте while и if.
# Попробуйте разные значения e и n.

print('Задание 5')
print('Посчитайте сумму чётных элементов от 1 до n исключая кратные e')
print('Т.к. в условии нет об этом уточнения, в моей реализации, числовая последовательность не будет включать n.\n')

while True:
    try:
        n = int(input('Введите целое полочительное число n, со значением от 2 и более (для создания числовой последовательности от 1 до n): '))
        if n <= 1:
            raise ValueError
        break
    except ValueError:
        print('Введены некорректные данные. Попробуйте еще раз.')

while True:
    try:
        e = int(input('Введите целое полочительное число e, со значением больше 0 (числа кратные e не будут учавствовать в вычислении): '))
        if e < 1:
            raise ValueError
        break
    except ValueError:
        print('Введены некорректные данные. Попробуйте еще раз.')

start = 1
res = 0
while start < n:
    if start % e and not start % 2:
        res += start
    start += 1

print(f'Ответ: сумма чисел от 1 до {n} (не включительно), не кратных {e} = {res}.\n')

